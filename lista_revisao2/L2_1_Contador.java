package poo.LISTA2;

public class L2_1_Contador {

    private int valor;

    public void zerarValor(int valor) {
        this.valor = 0;
    }

    public void incrementarValor(int valor) {
        this.valor += valor;
    }

    public int getValor() {
        return this.valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }
}
